package progetti;

public class tavola_multiplicazioni {

	public static void main(String[] args) {
		
		for (int i=1; i<=10 ; i++) {
			
			System.out.println("-------------");
			for (int a=1;a<=10; a++) {
				System.out.println(i+" x "+a+" = " + (i*a));
				System.out.println("");
				
			}
		}
		
	}

}
